# Manipulação de Lista 
vetorInteiros = [7, 12, 3, 15, 9, 1, 6, 21, 11, 2, 5]
print(f"\nVetor de nº inteiros ordenado: {sorted(vetorInteiros)}")

vetorInteiros.append(10)
print(f"\nVetor de nº inteiros com o 10 adicionado: {vetorInteiros}")

vetorInteiros.remove(5)
print(f'\nVetor de nº inteiros com o valor "5" removido: {vetorInteiros}')

